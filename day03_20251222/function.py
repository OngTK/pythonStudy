# 함수 Function

# 📌함수 =======================================================
# - 반복적인 코드를 줄여주고, 코드의 재사용성을 높여줌
# - 코드의 가독성을 높여줌

# 📌함수 기본 구조 ===============================================

# def 함수이름(매개변수1, 매개변수2):
#     """함수 설명문 (docstring)"""
#     # 함수 본문
#     실행문1
#     실행문2
#     return 반환값

# - 함수이름: 함수를 호출할 때 사용하는 이름
# - 매개변수: 함수에 입력으로 전달되는 값
# - docstring: 함수에 대한 설명을 작성하는 부분
# - 실행문: 함수가 호출될 때 실행되는 코드
# - return: 함수가 반환하는 값 (생략 가능)
#           return 즉시 함수 종료

# 📌함수 호출 ===================================================

# 함수이름(인자1, 인자2)

# - 인자: 함수에 전달되는 실제 값   
# - 함수를 호출하면 함수 본문이 실행되고, 반환값이 반환됨
# - 반환값이 없는 함수는 None을 반환함
# - 함수 호출 시 인자의 개수와 매개변수의 개수가 일치해야 함
# - 매개변수와 인자의 이름은 다를 수 있음

# 📌예제 ======================================================
def say_hello():
    print(f"Hello, Python!")

say_hello()  # 함수 호출

def greet(name):
    """주어진 이름으로 인사하는 함수"""
    print(f"Hello, {name}!")

greet("Alice")  # 함수 호출

def add(a, b):
    """두 수를 더하는 함수"""
    return a + b

result = add(3, 5)  # 함수 호출
print(f"3 + 5 = {result}")

# 📌지역변수 / 전역변수 ==========================================
# - 지역변수: 함수 내부에서 정의된 변수, 함수 외부에서는 접근 불가
# - 전역변수: 함수 외부에서 정의된 변수, 함수 내부에서도 접근 가능

x = 10  # 전역변수
def test():
    x = 5   # 지역변수
    print(f"지역변수 x: {x}") # 출력 5

test()
print(f"전역변수 x: {x}") # 출력 10
# ✔ 함수 안의 변수는 함수 밖에 영향을 주지 않음